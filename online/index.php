(function () {
	var elements;
	var tableu;
	var linesa;
	var linesb;
	var windowHeight;
	function init() {
		elements = document.querySelectorAll('.divs');
		tableu = document.querySelectorAll('.golds');
		linesa = document.querySelectorAll('.lones1');
		linesb = document.querySelectorAll('.lones2');
		prettys = document.querySelectorAll('.prettys');
		windowHeight = window.innerHeight;
	}
	function checkPosition() {
		for (var i = 0; i < elements.length; i++) {
			var element = elements[i];
			var positionFromTop = elements[i].getBoundingClientRect().top;

			if (positionFromTop - windowHeight <= 0) {
				element.classList.add('divone');
				element.classList.remove('divs');
			}
			else{
				element.classList.add('divs');
				element.classList.remove('divone');
			}
		}
		for (var j = 0; j < tableu.length; j++) {
			var tableux = tableu[j];
			var positionFromTop = tableu[j].getBoundingClientRect().top;

			if (positionFromTop - windowHeight <= 0) {
				tableux.classList.add('gold');
				tableux.classList.remove('golds');
			}
			else{
				tableux.classList.add('golds');
				tableux.classList.remove('gold');
			}
		}
		for (var i = 0; i < linesa.length; i++) {
			var linea = linesa[i];
			var positionFromTop = linesa[i].getBoundingClientRect().top;

			if (positionFromTop - windowHeight <= 0) {
				linea.classList.add('lines1');
				linea.classList.remove('lones1');
			}
			else{
				linea.classList.add('lones1');
				linea.classList.remove('lines1');
			}
		}
		for (var i = 0; i < linesb.length; i++) {
			var lineb = linesb[i];
			var positionFromTop = linesb[i].getBoundingClientRect().top;

			if (positionFromTop - windowHeight <= 0) {
				lineb.classList.add('lines2');
				lineb.classList.remove('lones2');
			}
			else{
				lineb.classList.add('lones2');
				lineb.classList.remove('lines2');
			}
		}
		for (var i = 0; i < prettys.length; i++) {
			var pretty = prettys[i];
			var positionFromTop = prettys[i].getBoundingClientRect().top;

			if (positionFromTop - windowHeight <= 0) {
				pretty.classList.add('pretty');
				pretty.classList.remove('prettys');
			}
			else{
				pretty.classList.add('prettys');
				pretty.classList.remove('pretty');
			}
		}
	}
	
	window.addEventListener('scroll', checkPosition);
	window.addEventListener('resize', init);

	init();
	checkPosition();
})();

